
GENERO = input("Digite su Genero: ")

if ( GENERO == "M") or (GENERO == "m"):
    print("Usted es un Hombre")
else:
    if ( GENERO == "F") or (GENERO == "f"):
        print("Usted es una Mujer")
    else:
        if ( GENERO == "O") or (GENERO == "o"):
            print("Otro")
        else:
            print("Error en Genero")    